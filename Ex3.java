
public class Ex3 {

	public static void main(String[] args) {
		System.out.println(1);//정수형(기본형=int) 리터럴
		System.out.println(3.14);//실수형(기본형 = double) 리터럴
		
		System.out.println('A');//문자형 리터럴
//		System.out.println('AB'); // 오류
		
		System.out.println(true);//논리형(boolean) 리터럴
//		System.out.println(TRUE);// 오류!
		
		System.out.println(100L);
		System.out.println(1.5f);
		System.out.println("Hello, World!");
		System.out.println("");
//		System.out.println('');//오류
		
	}

}
